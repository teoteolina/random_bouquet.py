import random

# список возможных цветов
flowers = ["роза", "орхидея", "тюльпан", "ирис", "нарцисс", "гвоздика", "пион", "лилия", "сирень"]

# запрос количества цветов в букете от пользователя
num_flowers = int(input("Сколько цветов вы хотите в букете? "))

# случайный выбор цветов для букета
bouquet = random.sample(flowers, num_flowers)

# вывод списка выбранных цветов
print("Ваш букет состоит из следующих цветов:")
for flower in bouquet:
    print(flower)
